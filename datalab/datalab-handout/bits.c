/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {

    unsigned int nShift = n << 3;
    unsigned int mShift = m << 3;

    unsigned int mask1 = 0xFF << nShift;
    unsigned int byte1 = (x & mask1) >> nShift;

    unsigned int mask2 = 0xFF << mShift;
    unsigned int byte2 = (x & mask2) >> mShift;

   return (x & ~(mask1 | mask2)) | (byte2 << nShift) | (byte1 << mShift);
}
